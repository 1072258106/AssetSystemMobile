package cn.shouma.bsytest.bean;

import java.io.Serializable;

public class Order implements Serializable {
	private int id;
	private int sendcode;
	private String customer;
	private String pCode;
	private String jxsName;
	private String outType;
	private int boxNum;
	private int orderstate;
	public Order() {
		super();
	}
	public Order(int sendcode, String customer, String pCode, String jxsName,
			String outType, int boxNum, int orderstate) {
		super();
		this.sendcode = sendcode;
		this.customer = customer;
		this.pCode = pCode;
		this.jxsName = jxsName;
		this.outType = outType;
		this.boxNum = boxNum;
		this.orderstate = orderstate;
	}
	
	public Order(int id, int sendcode, String customer, String pCode,
			String jxsName, String outType, int boxNum, int orderstate) {
		super();
		this.id = id;
		this.sendcode = sendcode;
		this.customer = customer;
		this.pCode = pCode;
		this.jxsName = jxsName;
		this.outType = outType;
		this.boxNum = boxNum;
		this.orderstate = orderstate;
	}
	public int getSendcode() {
		return sendcode;
	}
	public void setSendcode(int sendcode) {
		this.sendcode = sendcode;
	}
	public String getCustomer() {
		return customer;
	}
	public void setCustomer(String customer) {
		this.customer = customer;
	}
	public String getpCode() {
		return pCode;
	}
	public void setpCode(String pCode) {
		this.pCode = pCode;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getJxsName() {
		return jxsName;
	}
	public void setJxsName(String jxsName) {
		this.jxsName = jxsName;
	}
	public String getOutType() {
		return outType;
	}
	public void setOutType(String outType) {
		this.outType = outType;
	}
	public int getBoxNum() {
		return boxNum;
	}
	public void setBoxNum(int boxNum) {
		this.boxNum = boxNum;
	}
	public int getOrderstate() {
		return orderstate;
	}
	public void setOrderstate(int orderstate) {
		this.orderstate = orderstate;
	}
	@Override
	public String toString() {
		return "Order [id=" + id + ", sendcode=" + sendcode + ", customer="
				+ customer + ", pCode=" + pCode + ", jxsName=" + jxsName
				+ ", outType=" + outType + ", boxNum=" + boxNum
				+ ", orderstate=" + orderstate + "]";
	}
	
	
	
	
}
